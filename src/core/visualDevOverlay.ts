function renderPolishDevOverlayReadme(): string {
  return `# Game Polish Lab Dev Overlay

${polishDevOverlayGeneratedMarker}

This overlay is optional experimental developer tooling. Game Polish Lab remains a VS Code visual polish console; the normal workflow is still choose surface, preview and tune, save config/assets, direct apply where supported, then record the result.

The overlay is inert unless the game is opened with \`?polish=1\` or \`&polish=1\`. It should be included only during local development unless you intentionally choose otherwise.

## Manual Local Integration

Add the CSS and JS manually in your local development HTML or dev-only loader:

\`\`\`html
<link rel="stylesheet" href="/.game-polish-lab/dev-overlay/polish-dev-overlay.css">
<script src="/.game-polish-lab/dev-overlay/polish-dev-overlay.js"></script>
\`\`\`

Then open the game with:

\`\`\`text
http://localhost:3000/?polish=1
\`\`\`

The script emits \`game-polish-lab:tune-change\` browser \`CustomEvent\` messages with visual-only values. It does not mutate Phaser gameplay state, persist to game save data, call network APIs, or load external CDNs.

## Optional Config Payload

To display a detected config payload, set one of these globals before loading the overlay script:

\`\`\`js
window.__GAME_POLISH_LAB_CONFIG__ = { surface: "button", values: {} };
\`\`\`

## Removal

Remove the manual CSS/JS include and delete:

\`\`\`text
${polishDevOverlayRelativeDir}/
\`\`\`

production builds should exclude these files unless the developer intentionally includes them.
`;
}

function renderPolishDevOverlayManifest(generatedAt: Date): string {
  return `${JSON.stringify({
    marker: polishDevOverlayGeneratedMarker,
    schemaVersion: "game-polish-lab/dev-overlay/v1",
    generatedAt: generatedAt.toISOString(),
    activation: "URL query param polish=1",
    files: [
      polishDevOverlayScriptRelativePath,
      polishDevOverlayStyleRelativePath,
      polishDevOverlayReadmeRelativePath
    ]
  }, null, 2)}\n`;
}

function isSafeDevOverlayPath(relativePath: string): boolean {
  const normalized = normalizeVisualScopePath(relativePath);
  return normalized.startsWith(`${polishDevOverlayRelativeDir}/`)
    && !path.isAbsolute(normalized)
    && !normalized.split("/").includes("..");
}

function resolveWorkspacePath(workspaceRoot: string, relativePath: string): string | undefined {
  const normalized = normalizeVisualScopePath(relativePath);
  if (!normalized || path.isAbsolute(normalized) || normalized.split("/").includes("..")) {
    return undefined;
  }
  const root = path.resolve(workspaceRoot);
  const resolved = path.resolve(root, ...normalized.split("/"));
  return resolved === root || resolved.startsWith(`${root}${path.sep}`) ? resolved : undefined;
}
